CREATE TABLE ALUMNOS(
	ID_ALUMNO INT AUTO_INCREMENT,
    APELLIDO VARCHAR(50),
    NOMBRE VARCHAR(50),
    DOCUMENTO INT NOT NULL,
    PRIMARY KEY(ID_ALUMNO),
    UNIQUE KEY(APELLIDO, NOMBRE, DOCUMENTO)
);

CREATE TABLE COMUNICACIONES(
	ID_COMUNICACION INT AUTO_INCREMENT,
    ALUMNO INT,
    MENSAJE INT,
	PRIMARY KEY(ID_COMUNICACION),
    FOREIGN KEY(ALUMNO) REFERENCES ALUMNOS(ID_ALUMNO),
    FOREIGN KEY(ALUMNO) REFERENCES MENSAJES(ID_MENSAJE)
);